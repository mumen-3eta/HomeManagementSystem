import Vue from 'vue'
import Vuex from 'vuex'
import VuexPersistence from 'vuex-persist'
Vue.use(Vuex);

const state = {
    user : null
};

const store = new Vuex.Store({
    state,
    plugins: [new VuexPersistence().plugin],
    getters:{
        user :  (state) =>{
            return state.user;
        },
        GetUser :  (state) =>{
            return state.user.user;
        }
    },
    actions:{
        user(context,user){
            context.commit('user' , user );
        }
    },
    mutations:{
        user(state,user){
            state.user = user;
        }
    }

});

export default store;
